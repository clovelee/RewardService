package com.chinauicom.research.hfclient.app.entity;

public class Org {

	private String id;
	
	private String issueOrgId;
	
	private String name;
	
	private String role;
	
	private String totalNumber;
	
	private String restNumber;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getIssueOrgId() {
		return issueOrgId;
	}

	public void setIssueOrgId(String issueOrgId) {
		this.issueOrgId = issueOrgId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getTotalNumber() {
		return totalNumber;
	}

	public void setTotalNumber(String totalNumber) {
		this.totalNumber = totalNumber;
	}

	public String getRestNumber() {
		return restNumber;
	}

	public void setRestNumber(String restNumber) {
		this.restNumber = restNumber;
	}
	
}
